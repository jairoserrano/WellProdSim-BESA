package wpsPeasant.Goals.L1Survival;

import wpsPeasant.Tasks.L1Survival.HealthCareTask;
import BESA.BDI.AgentStructuralModel.GoalBDI;
import BESA.BDI.AgentStructuralModel.GoalBDITypes;
import BESA.BDI.AgentStructuralModel.StateBDI;
import BESA.Kernel.Agent.Event.KernellAgentEventExceptionBESA;
import BESA.Log.ReportBESA;
import rational.RationalRole;
import rational.mapping.Believes;
import rational.mapping.Plan;
import wpsPeasant.Agent.PeasantBDIAgentBelieves;
import wpsSimulator.wpsStart;

/**
 *
 * @author jairo
 */
public class SelfEvaluationGoal extends GoalBDI {

    /**
     *
     * @return
     */
    public static SelfEvaluationGoal buildGoal() {
        HealthCareTask healthCareTask = new HealthCareTask();
        Plan healthCarePlan = new Plan();
        healthCarePlan.addTask(healthCareTask);
        RationalRole healthCareRole = new RationalRole(
                "healthCareTask",
                healthCarePlan);
        SelfEvaluationGoal healthCareGoalBDI = new SelfEvaluationGoal(
                wpsStart.getPlanID(),
                healthCareRole,
                "healthCareTask",
                GoalBDITypes.SURVIVAL);
        return healthCareGoalBDI;
    }

    /**
     *
     * @param id
     * @param role
     * @param description
     * @param type
     */
    public SelfEvaluationGoal(long id, RationalRole role, String description, GoalBDITypes type) {
        super(id, role, description, type);
        //ReportBESA.info("");
    }

    /**
     *
     * @param parameters
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public double evaluateViability(Believes parameters) throws KernellAgentEventExceptionBESA {
        PeasantBDIAgentBelieves believes = (PeasantBDIAgentBelieves) parameters;
        ReportBESA.info("getHealthProgramsAvailability=" + believes.getPeasantProfile().getHealthProgramsAvailability());
        if (believes.getPeasantProfile().getHealthProgramsAvailability() > 0) {
            return 1;
        } else {
            return 0;
        }
    }

    /**
     *
     * @param parameters
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public double detectGoal(Believes parameters) throws KernellAgentEventExceptionBESA {
        PeasantBDIAgentBelieves believes = (PeasantBDIAgentBelieves) parameters;
        ReportBESA.info("getHealth=" + believes.getPeasantProfile().getHealth());
        if (believes.getPeasantProfile().getHealth() < 1) {
            return 1;
        } else {
            return 0;
        }
    }

    /**
     *
     * @param parameters
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public double evaluatePlausibility(Believes parameters) throws KernellAgentEventExceptionBESA {
        //ReportBESA.info("");
        PeasantBDIAgentBelieves believes = (PeasantBDIAgentBelieves) parameters;
        /*if (believes.getPeasantProfile().getHealth() > 0.0) {
            return 1;
        } else {
            return 0;
        }*/
        return 1;
    }

    /**
     *
     * @param stateBDI
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public double evaluateContribution(StateBDI stateBDI) throws KernellAgentEventExceptionBESA {
        //ReportBESA.info("");
        return 1;
    }

    /**
     *
     * @param stateBDI
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public boolean evaluateLegality(StateBDI stateBDI) throws KernellAgentEventExceptionBESA {
        //ReportBESA.info(stateBDI.getMachineBDIParams().getPyramidGoals());
        PeasantBDIAgentBelieves believes = (PeasantBDIAgentBelieves) stateBDI.getBelieves();
        return believes.getPeasantProfile().getHealth() > 0;
    }

    /**
     *
     * @param parameters
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public boolean goalSucceeded(Believes parameters) throws KernellAgentEventExceptionBESA {
        PeasantBDIAgentBelieves believes = (PeasantBDIAgentBelieves) parameters;
        ReportBESA.info("getHealth=" + believes.getPeasantProfile().getHealth());
        return believes.getPeasantProfile().getHealth()==1;
    }

}
