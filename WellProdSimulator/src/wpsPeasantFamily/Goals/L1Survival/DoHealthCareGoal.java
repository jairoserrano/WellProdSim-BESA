package wpsPeasantFamily.Goals.L1Survival;

import wpsPeasantFamily.Tasks.L1Survival.DoHealthCareTask;
import BESA.BDI.AgentStructuralModel.GoalBDI;
import BESA.BDI.AgentStructuralModel.GoalBDITypes;
import BESA.BDI.AgentStructuralModel.StateBDI;
import BESA.Kernel.Agent.Event.KernellAgentEventExceptionBESA;
import rational.RationalRole;
import rational.mapping.Believes;
import rational.mapping.Plan;
import wpsPeasantFamily.Agent.PeasantFamilyBDIAgentBelieves;
import wpsActivator.wpsStart;

/**
 *
 * @author jairo
 */
public class DoHealthCareGoal extends GoalBDI {

    /**
     *
     * @return
     */
    public static DoHealthCareGoal buildGoal() {
        DoHealthCareTask doHealthCareTask = new DoHealthCareTask();
        Plan doHealthCarePlan = new Plan();
        doHealthCarePlan.addTask(doHealthCareTask);
        RationalRole doHealthCareRole = new RationalRole(
                "DoHealthCareTask",
                doHealthCarePlan);
        DoHealthCareGoal doHealthCareGoal = new DoHealthCareGoal(
                wpsStart.getPlanID(),
                doHealthCareRole,
                "DoHealthCareTask",
                GoalBDITypes.SURVIVAL);
        return doHealthCareGoal;
    }

    /**
     *
     * @param id
     * @param role
     * @param description
     * @param type
     */
    public DoHealthCareGoal(long id, RationalRole role, String description, GoalBDITypes type) {
        super(id, role, description, type);
        //wpsReport.info("");
    }

    /**
     *
     * @param parameters
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public double evaluateViability(Believes parameters) throws KernellAgentEventExceptionBESA {
        PeasantFamilyBDIAgentBelieves believes = (PeasantFamilyBDIAgentBelieves) parameters;
        //wpsReport.info("getHealthProgramsAvailability=" + believes.getProfile().getHealthProgramsAvailability());
        if (believes.getPeasantProfile().getHealthProgramsAvailability() > 0) {
            return 1;
        } else {
            return 0;
        }
    }

    /**
     *
     * @param parameters
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public double detectGoal(Believes parameters) throws KernellAgentEventExceptionBESA {
        PeasantFamilyBDIAgentBelieves believes = (PeasantFamilyBDIAgentBelieves) parameters;
        //wpsReport.info("getHealth=" + believes.getProfile().getHealth());
        if (believes.getPeasantProfile().getHealth() ==0 ) {
            return 1;
        } else {
            return 0;
        }
    }

    /**
     *
     * @param parameters
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public double evaluatePlausibility(Believes parameters) throws KernellAgentEventExceptionBESA {
        //wpsReport.info("");
        PeasantFamilyBDIAgentBelieves believes = (PeasantFamilyBDIAgentBelieves) parameters;
        /*if (believes.getProfile().haveTimeAvailable(TimeConsumedBy.DoHealthCare)) {
            return 1;
        } else {
            return 0;
        }*/
        return 1;
    }

    /**
     *
     * @param stateBDI
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public double evaluateContribution(StateBDI stateBDI) throws KernellAgentEventExceptionBESA {
        //wpsReport.info("");
        return 1;
    }

    /**
     *
     * @param stateBDI
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public boolean evaluateLegality(StateBDI stateBDI) throws KernellAgentEventExceptionBESA {
        //wpsReport.info(stateBDI.getMachineBDIParams().getPyramidGoals());
        PeasantFamilyBDIAgentBelieves believes = (PeasantFamilyBDIAgentBelieves) stateBDI.getBelieves();
        return true;
        //return believes.getProfile().getHealth() > 0;
    }

    /**
     *
     * @param parameters
     * @return
     * @throws KernellAgentEventExceptionBESA
     */
    @Override
    public boolean goalSucceeded(Believes parameters) throws KernellAgentEventExceptionBESA {
        PeasantFamilyBDIAgentBelieves believes = (PeasantFamilyBDIAgentBelieves) parameters;
        //wpsReport.info("getHealth=" + believes.getProfile().getHealth());
        return believes.getPeasantProfile().getHealth() == 100 || believes.getPeasantProfile().getHealth() <= 0;
    }

}
